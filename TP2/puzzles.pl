printBoard(GameBoard) :-
    length(GameBoard, Size),
    nl,
    write('  '),
    printHeader(Size),
    printBoard(GameBoard, 0, Size).

printHeader(Size) :-
    printCode(0, Size),
    nl,
    write('  '),
    printRowSeparator(0, Size),
    nl.

printCode(Size, Size).

printCode(Act, Size) :-
    write('  '),
    write(Act),
    write(' '),
    NewAct is Act + 1,
    printCode(NewAct, Size).

printRowSeparator(Size, Size) :- write('|').

printRowSeparator(Act, Size) :-
    write('|---'),
    NewAct is Act + 1,
    printRowSeparator(NewAct, Size).

printBoard([], Size, Size).

printBoard([L|T], N, Size):-
    N1 is N + 1,
    write(N),
    write(' '),
    write('|'),
    print_line(L),  
    nl,
    write('  '),
    printRowSeparator(0, Size),
    nl,
    printBoard(T,N1, Size).

print_line([]).

print_line([C|L]):-
    print_cell(C), 
    print_line(L).
    
print_cell(C):-
    code(C,P),
    write(P),
    write(' |').

